numero = 60
acumulador = 0

for i in range(1, numero):
    resultado = numero + i
    print(f"{numero} + {i} = {resultado}")
    acumulador += 1  
    
for i in range(1, numero):
    resultado = numero - i
    print(f"{numero} - {i} = {resultado}")
    acumulador += 1  

for i in range(1, numero):
    resultado = numero * i
    print(f"{numero} * {i} = {resultado}")
    acumulador += 1  

for i in range(1, numero):
    resultado = numero / i
    print(f"{numero} / {i} = {resultado}")
    acumulador += 1  



    
    